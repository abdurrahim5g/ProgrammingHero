/**
 * In this file I will reverse the loop
 *
 * 1. First reverse in for loop
 * 2. While loop
 */

for (let i = 10; i > 0; i--) {
  console.log(i);
}
console.log("The for loop is end!");

/**
 * Now let's try to while loop
 */
let x = 10;
while (x > 0) {
  console.log(x);
  x--;
}
console.log("The While loop is end!");
